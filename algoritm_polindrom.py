### Пошаговый алгоритм:
#1. Привести строку к нижнему регистру для регистронезависимой проверки.
#2. Удалить все неалфавитные символы(например, пробелы, пунктуацию), чтобы оставить только буквы и цифры.
#3. Создать обратную версию строки.
#4. Сравнить оригинальную строку(после очистки) с её обратной версией.
#5. Вернуть `True`, если строки равны, и `False` в противном случае.

def is_palindrome(s):

    # 1. Привести строку к нижнему регистру для регистронезависимой проверки
    s = s.lower()

    # 2. Удалить все неалфавитные символы (например, пробелы, пунктуацию)
    s = ''.join(c for c in s if c.isalnum())

    # 3. Создать обратную версию строки
    reversed_s = s[::-1]

    # 4. Сравнить оригинальную строку с её обратной версией
    return s == reversed_s

# Примеры использования функции
print(f"A man, a plan, a canal, Panama: {is_palindrome('A man, a plan, a canal, Panama')}")  # True
print(f"Hello: {is_palindrome('Hello')}")  # False
print(f"Was it a car or a cat I saw?: {is_palindrome('Was it a car or a cat I saw?')}")  # True